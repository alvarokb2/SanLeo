     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $curso = Curso::create([
            'name' => $request->name]);
        $curso->save();

        $cursouser = CursoUser::create([
            'id_curso' => $curso->id,
            'id_user' => Auth::user()->id,
        ]);
        $cursouser->save();
        return Redirect::route('cursos.index');

  }

